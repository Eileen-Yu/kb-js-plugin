'use strict';

const {parseArgs} = require('../utils/parse');

function handleInit({command, args, universe}) {
  let {number} = parseArgs(args);

  if (!number) number = 1

  universe["initFile"] = `This is the file generated by init command, with number: ${number}`;

  return {
    apiVersion: "v1alpha1",
    command,
    universe,
  };
}


module.exports = {handleInit}
